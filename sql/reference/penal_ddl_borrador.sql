-- =========================================================
-- ASUNTO PENAL
-- =========================================================

CREATE TABLE public.penal_asuntos (
    id integer GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    delegacion_id integer NOT NULL,
    numero_carpeta text NOT NULL,
    anio_inicio integer NOT NULL,
    fecha_presentacion_denuncia date NOT NULL,
    delito_id integer NOT NULL,
    hechos_denunciante text NOT NULL,
    sin_cuantificar boolean NOT NULL DEFAULT true,
    cuantia_monto numeric(14,2),
    area_hechos_id integer,
    dato_relevante text,
    escenario_denunciante text NOT NULL,
    coadyuvancia boolean NOT NULL DEFAULT false,
    fecha_conocimiento_amp date,
    fecha_judicializacion date,
    determinacion_judicial text,
    estatus_general text NOT NULL DEFAULT 'TRAMITE',
    abogado_responsable_id integer,
    created_at timestamp with time zone NOT NULL DEFAULT now(),
    updated_at timestamp with time zone NOT NULL DEFAULT now(),
    activo boolean NOT NULL DEFAULT true,
    deleted_at timestamp with time zone,
    deleted_by integer,
    CONSTRAINT penal_asuntos_escenario_denunciante_check
        CHECK (escenario_denunciante IN ('IMSS', 'COADYUVANCIA', 'DISTINTO_IMSS')),
    CONSTRAINT penal_asuntos_estatus_general_check
        CHECK (estatus_general IN ('TRAMITE', 'CONCLUIDO')),
    CONSTRAINT penal_asuntos_cuantia_check
        CHECK (
            (sin_cuantificar = true AND cuantia_monto IS NULL)
            OR
            (sin_cuantificar = false)
        ),
    CONSTRAINT penal_asuntos_anio_inicio_check
        CHECK (anio_inicio >= 2000),
    CONSTRAINT penal_asuntos_numero_carpeta_unique UNIQUE (numero_carpeta),
    CONSTRAINT penal_asuntos_delegacion_fk
        FOREIGN KEY (delegacion_id) REFERENCES public.delegaciones(id),
    CONSTRAINT penal_asuntos_delito_fk
        FOREIGN KEY (delito_id) REFERENCES public.delitos(id),
    CONSTRAINT penal_asuntos_area_hechos_fk
        FOREIGN KEY (area_hechos_id) REFERENCES public.areas(id),
    CONSTRAINT penal_asuntos_abogado_responsable_fk
        FOREIGN KEY (abogado_responsable_id) REFERENCES public.usuarios(id),
    CONSTRAINT penal_asuntos_deleted_by_fk
        FOREIGN KEY (deleted_by) REFERENCES public.usuarios(id)
);

CREATE INDEX idx_penal_asuntos_delegacion ON public.penal_asuntos(delegacion_id);
CREATE INDEX idx_penal_asuntos_delito ON public.penal_asuntos(delito_id);
CREATE INDEX idx_penal_asuntos_estatus_general ON public.penal_asuntos(estatus_general);
CREATE INDEX idx_penal_asuntos_fecha_presentacion ON public.penal_asuntos(fecha_presentacion_denuncia);

