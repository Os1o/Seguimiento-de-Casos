CREATE TABLE public.penal_requerimientos (
    id integer GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    asunto_id integer NOT NULL,
    folio_referencia text,
    autoridad_emisora text NOT NULL,
    fecha_recepcion date NOT NULL,
    fecha_limite_atencion date NOT NULL,
    fecha_inicio_interno date,
    area_responsable_id integer,
    fase_actual text NOT NULL DEFAULT 'ALTA_INICIAL',
    created_by integer,
    created_at timestamp with time zone NOT NULL DEFAULT now(),
    updated_at timestamp with time zone NOT NULL DEFAULT now(),
    activo boolean NOT NULL DEFAULT true,
    CONSTRAINT penal_requerimientos_fase_actual_check
        CHECK (fase_actual IN ('ALTA_INICIAL', 'SEGUIMIENTO_INTERNO', 'CONTESTACION_FINAL', 'CERRADO')),
    CONSTRAINT penal_requerimientos_asunto_fk
        FOREIGN KEY (asunto_id) REFERENCES public.penal_asuntos(id) ON DELETE CASCADE,
    CONSTRAINT penal_requerimientos_area_responsable_fk
        FOREIGN KEY (area_responsable_id) REFERENCES public.areas(id),
    CONSTRAINT penal_requerimientos_created_by_fk
        FOREIGN KEY (created_by) REFERENCES public.usuarios(id)
);

CREATE INDEX idx_penal_requerimientos_asunto ON public.penal_requerimientos(asunto_id, created_at DESC);
CREATE INDEX idx_penal_requerimientos_fase ON public.penal_requerimientos(fase_actual);

